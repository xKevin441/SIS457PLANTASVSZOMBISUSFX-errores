#include "Calabaza.h"

void Calabaza::DefenzaCalabaza()
{
	posicionX = 1;
	for (int i = 0; i <= 10; i++)
	{
		posicionX == posicionY;
	}
	for (int i = 0; i <= 144; i--)
	{
		posicionX -= posicionX - vida;
		posicionY -= posicionY - vida;
	}

	for (int i = 0; i <= 144; i--)
	{
		estado -= estado - vida;
	}
	for (int i = 0; i = vida; i++)
	{
		potenciar += potenciar + vida;
	}
}
